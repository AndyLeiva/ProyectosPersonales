print("Bienvendo a la calculadora que suma numeros con potencias\n")


base1 = int(input("Ingrese el primer numero: "))
expontente1 = int(input("Ingrese el expontente para el primer numero: "))

base2 = int(input("Ingrese el segundo numero: "))
expontente2 = int(input("Ingrese el expontente para el segundo numero: "))

base3 = int(input("Ingrese el tercer numero: "))
expontente3 = int(input("Ingrese el expontente para el tercer numero: "))


resultado = base1**expontente1+base2**expontente2+base3**expontente3 

